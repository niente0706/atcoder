#include <bits/stdc++.h>
#include <atcoder/all>

using namespace std;
using namespace atcoder;

int main() {
    // 一年の秒数
    int seconds = 365 * 24 * 60 * 60;

    // 1年
    cout << seconds << endl;
    // 2年
    cout << 2 * seconds << endl;
    // 5年
    cout << 5 * seconds << endl;
    // 10年
    cout << 10 * seconds << endl;
}
